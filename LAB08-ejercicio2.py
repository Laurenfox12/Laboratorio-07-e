"""
El módulo 'sys' es un módulo incorporado en Python que proporciona acceso a algunas variables y funciones
que se usa principalmente para realizar operaciones y acceder a información relacionada con el sistema y el entorno 
en el que se está ejecutando el programa Python.

Algunas de las funciones y variables más comunes proporcionadas por el módulo 'sys' son:

1. sys.argv: Lista de argumentos pasados al script de Python desde la línea de comandos.
2. sys.exit([status]): Termina el programa y sale del intérprete de Python. El argumento opcional 'status'
   puede ser un valor entero que se devuelve al sistema operativo.
3. sys.version: Devuelve una cadena que contiene la versión de Python.
4. sys.path: Lista de rutas en las que Python busca los módulos importados.
5. sys.modules: Un diccionario que contiene los módulos que ya han sido importados.
6. sys.stdin, sys.stdout, sys.stderr: Flujo estándar de entrada, salida y error respectivamente.

"""